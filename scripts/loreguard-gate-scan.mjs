function buildRouteInventory(files) {
  const pages = [];
  const apis = [];
  for (const absPath of files) {
    const pageRoute = routeFromAppPage(absPath);
    if (pageRoute) pages.push({ route: pageRoute === '/' ? '/' : pageRoute, file: relPath(absPath) });
    const apiRoute = routeFromApi(absPath);
    if (apiRoute) apis.push({ route: apiRoute, file: relPath(absPath) });
  }
  pages.sort((a, b) => a.route.localeCompare(b.route));
  apis.sort((a, b) => a.route.localeCompare(b.route));
  return { pages, apis };
}

function classifyApiCapabilities(apiRows) {
  return apiRows.map((row) => {
    const source = readText(join(ROOT, row.file));
    const capabilities = TOOL_CAPABILITY_PATTERNS
      .filter((pattern) => pattern.regex.test(source) || pattern.regex.test(row.route))
      .map((pattern) => pattern.id);
    const risk =
      capabilities.includes('destructive-action') ||
      capabilities.includes('payment-or-stripe') ||
      capabilities.includes('proxy-or-egress')
        ? 'H'
        : capabilities.length > 0
          ? 'M'
          : 'L';
    return { ...row, risk, capabilities };
  });
}

function groupFindingCounts(findings) {
  const rows = new Map();
  for (const finding of findings) {
    const key = `${finding.severity}:${finding.pattern}`;
    const prev = rows.get(key) ?? { severity: finding.severity, pattern: finding.pattern, count: 0 };
    prev.count += 1;
    rows.set(key, prev);
  }
  return [...rows.values()].sort((a, b) => {
    const order = { C: 0, H: 1, M: 2, I: 3 };
    return (order[a.severity] ?? 9) - (order[b.severity] ?? 9) || a.pattern.localeCompare(b.pattern);
  });
}

