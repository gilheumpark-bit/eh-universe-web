function mdTable(headers, rows) {
  const head = `| ${headers.join(' | ')} |`;
  const sepLine = `| ${headers.map(() => '---').join(' | ')} |`;
  const body = rows.map((row) => `| ${row.map((cell) => String(cell).replace(/\n/g, ' ')).join(' | ')} |`);
  return [head, sepLine, ...body].join('\n');
}

function renderFindingTable(findings) {
  if (findings.length === 0) return '없음';
  return mdTable(
    ['Severity', 'Pattern', 'File', 'Line', 'Snippet'],
    topFindings(findings).map((hit) => [hit.severity, hit.pattern, hit.file, hit.line, `\`${hit.snippet}\``]),
  );
}

function renderHtmlInsertionTable(findings) {
  if (findings.length === 0) return '없음';
  return mdTable(
    ['Status', 'File', 'Line', 'Evidence'],
    findings.map((hit) => [hit.status, `\`${hit.file}\``, hit.line, hit.evidence]),
  );
}

function renderApiTable(rows) {
  if (rows.length === 0) return '없음';
  return mdTable(
    ['Risk', 'Route', 'Capabilities', 'File'],
    rows.map((row) => [row.risk, `\`${row.route}\``, row.capabilities.join(', ') || '-', `\`${row.file}\``]),
  );
}

function renderRouteTable(rows) {
  if (rows.length === 0) return '없음';
  return mdTable(['Route', 'File'], rows.map((row) => [`\`${row.route}\``, `\`${row.file}\``]));
}

function fileContains(relFile, needles) {
  const absPath = join(ROOT, relFile);
  if (!existsSync(absPath)) return false;
  const text = readText(absPath);
  return needles.every((needle) => text.includes(needle));
}

