function toPosix(value) {
  return value.split(sep).join('/');
}

function formatKst(date) {
  const formatter = new Intl.DateTimeFormat('sv-SE', {
    timeZone: 'Asia/Seoul',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit',
    hour12: false,
  });
  return `${formatter.format(date).replace(' ', 'T')}+09:00`;
}

function relPath(absPath) {
  return toPosix(relative(ROOT, absPath));
}

function readJsonIfExists(relFile) {
  const absPath = join(ROOT, relFile);
  if (!existsSync(absPath)) return null;
  try {
    return JSON.parse(readFileSync(absPath, 'utf8'));
  } catch {
    return null;
  }
}

function readReleaseEvidenceStatus() {
  const relFile = process.env.LOREGUARD_RELEASE_EVIDENCE_STATUS || RELEASE_EVIDENCE_STATUS_FILE;
  const payload = readJsonIfExists(relFile);
  if (!payload || typeof payload !== 'object' || Array.isArray(payload)) {
    return {
      file: relFile,
      verdict: DEFAULT_RELEASE_VERDICT,
      valid: false,
      reason: 'release evidence status artifact is missing or invalid',
    };
  }
  if (payload.kind !== 'loreguard.release-evidence-status.v1') {
    return {
      file: relFile,
      verdict: DEFAULT_RELEASE_VERDICT,
      valid: false,
      reason: 'release evidence status artifact kind mismatch',
    };
  }
  return {
    file: relFile,
    verdict: payload.verdict === 'PASS' ? 'PASS' : DEFAULT_RELEASE_VERDICT,
    valid: true,
    reason: payload.verdict === 'PASS'
      ? 'all required release evidence artifacts passed intake validation'
      : 'release evidence intake verdict is HOLD',
    counts: payload.counts && typeof payload.counts === 'object' ? payload.counts : {},
  };
}

function computeReleaseVerdict(result) {
  if (result.blockerCount > 0) return 'HOLD';
  return result.releaseEvidenceStatus?.verdict === 'PASS' ? 'PASS' : DEFAULT_RELEASE_VERDICT;
}

function buildReleaseReason(result) {
  if (result.blockerCount > 0) {
    return 'Static blocker findings exist in the Day 0 scan.';
  }
  if (result.releaseEvidenceStatus?.verdict === 'PASS') {
    return 'Day 0 static baseline and release evidence intake are both PASS.';
  }
  const reason = result.releaseEvidenceStatus?.reason ?? 'release evidence status artifact is missing';
  return `T0~T15 local baseline is clean, but ${reason}.`;
}

function isTestPath(absPath) {
  const rel = relPath(absPath);
  return rel.includes('/__tests__/') || /\.test\./.test(rel) || /\.spec\./.test(rel);
}

function* walkFiles(dir, exts = SOURCE_EXTS) {
  if (!existsSync(dir)) return;
  for (const entry of readdirSync(dir)) {
    const absPath = join(dir, entry);
    const stat = statSync(absPath);
    if (stat.isDirectory()) {
      if (SKIP_DIRS.has(entry)) continue;
      yield* walkFiles(absPath, exts);
      continue;
    }
    if (exts.has(extname(entry))) yield absPath;
  }
}

function readText(absPath) {
  return readFileSync(absPath, 'utf8');
}

function routeFromAppPage(absPath) {
  const rel = relPath(absPath);
  if (!rel.startsWith('src/app/') || !rel.endsWith('/page.tsx')) return null;
  if (rel.startsWith('src/app/api/')) return null;
  const withoutPrefix = rel.slice('src/app/'.length, -'/page.tsx'.length);
  const segments = withoutPrefix
    .split('/')
    .filter(Boolean)
    .filter((segment) => !(segment.startsWith('(') && segment.endsWith(')')));
  return '/' + segments.join('/');
}

function routeFromApi(absPath) {
  const rel = relPath(absPath);
  if (!rel.startsWith('src/app/api/') || !/\/route\.(ts|tsx)$/.test(rel)) return null;
  const withoutPrefix = rel.slice('src/app/api/'.length).replace(/\/route\.(ts|tsx)$/, '');
  return `/api/${withoutPrefix}`;
}

function firstLineSnippet(line) {
  return line.trim().replace(/\s+/g, ' ').slice(0, 180);
}

function isCommentOnlyLine(line) {
  const trimmed = line.trim();
  return trimmed.startsWith('//') || trimmed.startsWith('/*') || trimmed.startsWith('*') || trimmed.startsWith('{/*');
}

function isSecurityDetectorDefinition(absPath, line) {
  return relPath(absPath) === 'src/lib/security-gate.ts' && /\bregex:\s*\//.test(line);
}

function shouldSkipPatternLine(absPath, patternId, line, options) {
  if (options.skipCommentOnly && isCommentOnlyLine(line)) return true;
  if (options.skipSecurityDetectorDefinitions && isSecurityDetectorDefinition(absPath, line)) return true;
  return false;
}

function countByTopDir(files, prefix) {
  const counts = new Map();
  for (const absPath of files) {
    const rel = relPath(absPath);
    if (!rel.startsWith(prefix)) continue;
    const rest = rel.slice(prefix.length).replace(/^\/+/, '');
    const key = rest.split('/')[0] || '(root)';
    counts.set(key, (counts.get(key) ?? 0) + 1);
  }
  return [...counts.entries()].sort((a, b) => a[0].localeCompare(b[0]));
}

