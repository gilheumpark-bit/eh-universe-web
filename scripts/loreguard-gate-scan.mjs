// PART 3 — Scanners
// ============================================================
// Role:    Produce static findings and inventories.
// Banned:  Treating static scan as final release PASS.
// Input:   Source file list and pattern sets.
// Output:  Finding arrays and inventory rows.
// Depends: PART 2 helpers.
// ============================================================

function scanPatterns(files, patterns, limitPerPattern = 100, options = {}) {
  const out = [];
  const scanOptions = {
    skipCommentOnly: false,
    skipSecurityDetectorDefinitions: false,
    ...options,
  };
  const countByPattern = new Map();
  for (const absPath of files) {
    const text = readText(absPath);
    const lines = text.split(/\r?\n/);
    lines.forEach((line, lineIndex) => {
      for (const pattern of patterns) {
        const currentCount = countByPattern.get(pattern.id) ?? 0;
        if (currentCount >= limitPerPattern) continue;
        if (shouldSkipPatternLine(absPath, pattern.id, line, scanOptions)) continue;
        pattern.regex.lastIndex = 0;
        if (!pattern.regex.test(line)) continue;
        countByPattern.set(pattern.id, currentCount + 1);
        out.push({
          pattern: pattern.id,
          severity: pattern.severity ?? 'I',
          file: relPath(absPath),
          line: lineIndex + 1,
          snippet: firstLineSnippet(line),
        });
      }
    });
  }
  return out;
}

