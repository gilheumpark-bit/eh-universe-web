function buildResult() {
  const allSourceFiles = [...walkFiles(SRC_ROOT, SOURCE_EXTS)];
  const codeFiles = allSourceFiles.filter((file) => CODE_EXTS.has(extname(file)));
  const productionCodeFiles = codeFiles.filter((file) => !isTestPath(file));
  const routes = buildRouteInventory(codeFiles);
  const apiCapabilities = classifyApiCapabilities(routes.apis);
  const removedSurfaceFindings = scanPatterns(productionCodeFiles, REMOVED_SURFACE_PATTERNS, 100, { skipCommentOnly: true })
    .filter((finding) => !isAllowedRemovedSurfaceGuard(finding));
  const dangerousFindings = scanPatterns(productionCodeFiles, DANGEROUS_PATTERNS, 100, {
    skipCommentOnly: true,
    skipSecurityDetectorDefinitions: true,
  });
  const htmlInsertionFindings = annotateHtmlInsertionFindings(
    scanPatterns(productionCodeFiles, HTML_INSERTION_PATTERNS, 100, { skipCommentOnly: true }),
  );
  const stubFindings = scanPatterns(productionCodeFiles, STUB_PATTERNS);
  const trustLabelFindings = scanPatterns(productionCodeFiles, TRUST_LABEL_PATTERNS, 250, { skipCommentOnly: true });
  const blockerCount = [...removedSurfaceFindings, ...dangerousFindings].filter((hit) => hit.severity === 'C' || hit.severity === 'H').length;

  return {
    generatedAt: NOW,
    releaseEvidenceStatus: readReleaseEvidenceStatus(),
    counts: {
      sourceFiles: allSourceFiles.length,
      productionCodeFiles: productionCodeFiles.length,
    },
    routes,
    apiCapabilities,
    componentCounts: countByTopDir(allSourceFiles, 'src/components'),
    libCounts: countByTopDir(allSourceFiles, 'src/lib'),
    removedSurfaceFindings,
    dangerousFindings,
    htmlInsertionFindings,
    stubFindings,
    trustLabelFindings,
    blockerCount,
  };
}

function parseArgs(argv) {
  const args = { json: false, writePath: '', failOnHold: false, selfTest: false };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--json') args.json = true;
    if (arg === '--fail-on-hold') args.failOnHold = true;
    if (arg === '--self-test') args.selfTest = true;
    if (arg === '--write') {
      const next = argv[index + 1];
      if (!next) throw new Error('--write requires a path');
      args.writePath = next;
      index += 1;
    }
  }
  return args;
}

function shouldFailForHold(result) {
  const baselineVerdict = result.blockerCount > 0 ? 'HOLD' : 'PASS';
  return baselineVerdict === 'HOLD' || computeReleaseVerdict(result) === 'HOLD';
}

function requireSelfTest(condition, message) {
  if (!condition) throw new Error(`[self-test] ${message}`);
}

function runSelfTest() {
  const passResult = {
    blockerCount: 0,
    releaseEvidenceStatus: {
      verdict: 'PASS',
      valid: true,
      file: 'self-test/release-evidence-status.json',
    },
  };
  const evidenceHoldResult = {
    blockerCount: 0,
    releaseEvidenceStatus: {
      verdict: 'HOLD',
      valid: true,
      file: 'self-test/release-evidence-status.json',
    },
  };
  const staticHoldResult = {
    blockerCount: 1,
    releaseEvidenceStatus: {
      verdict: 'PASS',
      valid: true,
      file: 'self-test/release-evidence-status.json',
    },
  };
  const missingEvidenceResult = {
    blockerCount: 0,
    releaseEvidenceStatus: {
      verdict: DEFAULT_RELEASE_VERDICT,
      valid: false,
      file: 'self-test/missing.json',
    },
  };

  requireSelfTest(computeReleaseVerdict(passResult) === 'PASS', 'PASS evidence with zero blockers should release PASS');
  requireSelfTest(!shouldFailForHold(passResult), 'PASS evidence with zero blockers should not fail on hold');
  requireSelfTest(computeReleaseVerdict(evidenceHoldResult) === 'HOLD', 'HOLD evidence should keep release HOLD');
  requireSelfTest(shouldFailForHold(evidenceHoldResult), 'HOLD evidence should fail on hold');
  requireSelfTest(computeReleaseVerdict(staticHoldResult) === 'HOLD', 'static blockers should override PASS evidence');
  requireSelfTest(shouldFailForHold(staticHoldResult), 'static blockers should fail on hold');
  requireSelfTest(computeReleaseVerdict(missingEvidenceResult) === 'HOLD', 'missing evidence should default to HOLD');

  return {
    kind: 'loreguard.day0-gate-self-test.v1',
    status: 'PASS',
    checks: [
      'PASS evidence with zero blockers releases PASS',
      'HOLD evidence fails --fail-on-hold',
      'static blockers override release evidence PASS',
      'missing evidence defaults to HOLD',
    ],
  };
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.selfTest) {
    process.stdout.write(`${JSON.stringify(runSelfTest(), null, 2)}\n`);
    return;
  }
  const result = buildResult();
  const output = args.json ? `${JSON.stringify(result, null, 2)}\n` : buildMarkdownReport(result);

  if (args.writePath) {
    const absPath = resolve(ROOT, args.writePath);
    if (!absPath.startsWith(ROOT)) throw new Error(`Refusing to write outside repo: ${absPath}`);
    mkdirSync(dirname(absPath), { recursive: true });
    writeFileSync(absPath, output, 'utf8');
  }

  process.stdout.write(output);
  if (args.failOnHold && shouldFailForHold(result)) process.exitCode = 1;
}

main();
