// ── Translation dictionaries ──

const dictionaries: Record<Locale, Record<string, string>> = { ko, en };

// ── Runtime state ──

let currentLocale: Locale = DEFAULT_LOCALE;
let initialized = false;

function init() {
  if (initialized) return;
  initialized = true;
  if (typeof window === "undefined") return;
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === "ko" || stored === "en") {
      currentLocale = stored;
    }
  } catch {
    /* ignore */
  }
}

// ── Public API ──

export function getLocale(): Locale {
  init();
  return currentLocale;
}

export function setLocale(locale: Locale): void {
  init();
  if (locale !== "ko" && locale !== "en") return;
  currentLocale = locale;
  if (typeof window !== "undefined") {
    try {
      localStorage.setItem(STORAGE_KEY, locale);
    } catch {
      /* ignore */
    }
    window.dispatchEvent(new CustomEvent(CHANGE_EVENT, { detail: { locale } }));
  }
}

/**
 * Translate a key with optional parameter interpolation.
 * Example: t('ai.contextWarning', { percent: 80 }) → "컨텍스트 창의 80%를 사용 중입니다"
 */
export function t(key: string, params?: Record<string, string | number>): string {
  init();
  const primary = dictionaries[currentLocale];
  const fallback = dictionaries[currentLocale === "ko" ? "en" : "ko"];

  let text = primary[key] ?? fallback[key] ?? key;

  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }

  return text;
}

// ── React hook ──

import { useState, useEffect, useCallback, useRef } from "react";

export function useLocale() {
  const [locale, setLocaleState] = useState<Locale>(getLocale);

  useEffect(() => {
    const handler = (e: Event) => {
      const detail = (e as CustomEvent).detail;
      if (detail?.locale) setLocaleState(detail.locale);
    };
    window.addEventListener(CHANGE_EVENT, handler);
    return () => window.removeEventListener(CHANGE_EVENT, handler);
  }, []);

  const changeLocale = useCallback((l: Locale) => {
    setLocale(l);
    setLocaleState(l);
  }, []);

  const translate = useCallback(
    (key: string, params?: Record<string, string | number>) => t(key, params),
    // eslint-disable-next-line react-hooks/exhaustive-deps
    [locale],
  );

  return { locale, setLocale: changeLocale, t: translate };
}
