/**
 * Sanitize a string for use as a filename.
 * Replaces unsafe characters and trims whitespace.
 */
function sanitizeFilename(name: string): string {
  return (name || 'unnamed')
    .replace(/[/\\?%*:|"<>]/g, '-')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 100)
    || 'unnamed';
}
