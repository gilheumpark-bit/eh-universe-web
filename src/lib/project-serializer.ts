// PART 5 — Repo Files -> Config (Deserialize)
// ============================================================

/**
 * Deserialize repo files back into a partial StoryConfig.
 * Handles missing files gracefully — only populates fields for files found.
 */
export function repoFilesToConfig(files: RepoFile[]): Partial<StoryConfig> {
  if (!files || files.length === 0) return {};

  const result: Partial<StoryConfig> = {};

  // Index files by path for fast lookup
  const fileMap = new Map<string, string>();
  for (const f of files) {
    if (f?.path && f.content != null) {
      fileMap.set(f.path, f.content);
    }
  }

  // --- novel.yaml ---
  const novelRaw = fileMap.get('novel.yaml');
  if (novelRaw) {
    const core = fromYaml<Record<string, unknown>>(novelRaw);
    if (core) {
      if (core.genre !== undefined) result.genre = core.genre as StoryConfig['genre'];
      if (core.title !== undefined) result.title = String(core.title);
      if (typeof core.totalEpisodes === 'number') result.totalEpisodes = core.totalEpisodes;
      if (typeof core.episode === 'number') result.episode = core.episode;
      if (core.synopsis !== undefined) result.synopsis = String(core.synopsis);
      if (core.povCharacter !== undefined) result.povCharacter = String(core.povCharacter);
      if (core.setting !== undefined) result.setting = String(core.setting);
      if (core.primaryEmotion !== undefined) result.primaryEmotion = String(core.primaryEmotion);
      if (core.platform !== undefined) result.platform = core.platform as StoryConfig['platform'];
      if (core.narrativeIntensity !== undefined) result.narrativeIntensity = core.narrativeIntensity as StoryConfig['narrativeIntensity'];
      if (core.guardrails !== undefined) result.guardrails = core.guardrails as StoryConfig['guardrails'];
      if (Array.isArray(core.subGenres)) result.subGenres = core.subGenres as string[];
      if (core.prismMode !== undefined) result.prismMode = core.prismMode as StoryConfig['prismMode'];
    }
  }

  // --- characters/*.yaml ---
  const characters: Character[] = [];
  for (const [path, content] of fileMap) {
    if (path.startsWith('characters/') && path.endsWith('.yaml')) {
      const char = yamlToCharacter(content);
      if (char.name) characters.push(char);
    }
  }
  if (characters.length > 0) result.characters = characters;

  // --- world/setting.yaml ---
  const worldRaw = fileMap.get('world/setting.yaml');
  if (worldRaw) {
    const world = fromYaml<Record<string, string>>(worldRaw);
    if (world) {
      const worldKeys = [
        'corePremise', 'powerStructure', 'currentConflict',
        'worldHistory', 'socialSystem', 'economy', 'magicTechSystem',
        'factionRelations', 'survivalEnvironment',
        'culture', 'religion', 'education', 'lawOrder', 'taboo',
        'dailyLife', 'travelComm', 'truthVsBeliefs',
      ] as const;
      for (const key of worldKeys) {
        if (world[key] !== undefined) {
          (result as Record<string, unknown>)[key] = world[key];
        }
      }
    }
  }

  // --- direction/scene-direction.yaml ---
  const directionRaw = fileMap.get('direction/scene-direction.yaml');
  if (directionRaw) {
    const direction = fromYaml<SceneDirectionData>(directionRaw);
    if (direction) result.sceneDirection = direction;
  }

  // --- volumes/vol-XX/ep-XXX.md (원본) ---
  const manuscripts: EpisodeManuscript[] = [];
  for (const [path, content] of fileMap) {
    if (path.startsWith('volumes/') && path.endsWith('.md')) {
      const ms = markdownToEpisode(content, path);
      manuscripts.push(ms);
    }
  }
  if (manuscripts.length > 0) {
    manuscripts.sort((a, b) => a.episode - b.episode);
    result.manuscripts = manuscripts;
  }

  // --- translations/<lang>/volumes/vol-XX/ep-XXX.md (번역본) ---
  const translations: TranslatedManuscriptEntry[] = [];
  for (const [path, content] of fileMap) {
    if (path.startsWith('translations/') && path.endsWith('.md')) {
      const t = markdownToTranslatedManuscript(content, path);
      if (t) translations.push(t);
    }
  }
  if (translations.length > 0) {
    translations.sort((a, b) =>
      a.targetLang === b.targetLang ? a.episode - b.episode : a.targetLang.localeCompare(b.targetLang)
    );
    result.translatedManuscripts = translations;
  }

  return result;
}

/**
 * Repo 파일에서 .noa/profile.json 을 추출해 WriterProfile 로 복원.
 * GitHub pull 후 StudioShell 등에서 호출 → saveProfile(profile)로 localStorage 반영.
 * 파일 없으면 null.
 */
export function extractWriterProfile(files: RepoFile[]): WriterProfile | null {
  if (!files || files.length === 0) return null;
  const file = files.find((f) => f?.path === '.noa/profile.json');
  if (!file || typeof file.content !== 'string') return null;
  return jsonToWriterProfile(file.content);
}

// ============================================================
// PART 6 — Writer Profile Helpers
// ============================================================

/**
 * Serialize a WriterProfile to a JSON string for the .noa/profile.json file.
 */
export function writerProfileToJson(profile: WriterProfile): string {
  return JSON.stringify(profile, null, 2) + '\n';
}

/**
 * Parse a JSON string back to a WriterProfile.
 */
export function jsonToWriterProfile(json: string): WriterProfile | null {
  try {
    return JSON.parse(json) as WriterProfile;
  } catch (err) {
    logger.warn('project-serializer', 'jsonToWriterProfile parse failed', err);
    return null;
  }
}

